      <h2><strong>Overview</strong></h2>

      <h3><p><strong>The ADBâ€™s Technical Assistance TA-8761
  is designed to assist the Solomon Islands Government to enhance their capability for effective macroeconomic policy through improved macroeconomic modelling, analytical research, and economic reform strategy. 
</p><p>The ADB expert is working with officials from the Ministry of Finance and Treasury (MOFT), Economic Reform Unit (ERU) and other government offices to: 

<li>build capacity to analyze the macroeconomic outlook and produce accurate forecasts; </li>
<li>prepare policy research based on sound analytical methods; </li>
<li>assist the government to develop its economic reform agenda. </li>

</p><p>The capacity building component of the TA comprises a Boot  Camp program to develop and deliver training modules focused on basic methods in econometrics, using the Eviews statistical software. The Boot Camp provides lectures and hands-on Workshops for new analysts and material to support future boot-camp facilitators (course curriculum, presentation slides, practical training exercises, background readings etc.).
